#include "../minishell.h"

int	ft_strncmp(char *s1, char *s2, int len)
{
	int	i;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0' && len > 0)
	{
		if (s1[i] != s2[i])
		{
			break ;
		}
		i++;
		len--;
	}
	if (len == 0)
		return (0);
	return (1);
}
